import numpy as np
values = np.array([2, 4, 6, 8, 10])
print("Array --- ", values)
print("SQRT ---",np.sqrt(values))
print("Exponential ---", np.exp(values))
print("Logorithm ---",np.log(values))
print("Sum of all element ----",np.sum(values))
print("Cumulative Sum ----",np.cumulative_sum(values))

